import UIKit

extension UIColor {
    static var trBackground: UIColor { UIColor(named: "Tracker Background") ?? UIColor.darkGray }
    static var trBlack: UIColor { UIColor(named: "Tracker Black") ?? UIColor.black }
    static var trBlue: UIColor { UIColor(named: "Tracker Blue") ?? UIColor.blue }
    static var trGray: UIColor { UIColor(named: "Tracker Gray") ?? UIColor.gray }
    static var trLightGray: UIColor { UIColor(named: "Tracker Light Gray") ?? UIColor.lightGray }
    static var trRed: UIColor { UIColor(named: "Tracker Red") ?? UIColor.red }
    static var trWhite: UIColor { UIColor(named: "Tracker White") ?? UIColor.white }
}
